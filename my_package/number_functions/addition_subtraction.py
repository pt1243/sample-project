__all__ = ["add_one", "subtract_one"]


def add_one(x: float) -> float:
    """Increment a number.

    Parameters
    ----------
    x : float
        The number to increment.

    Returns
    -------
    float
    """
    return x + 1


def subtract_one(x: float) -> float:
    """Decrement a number.

    Parameters
    ----------
    x : float
        The number to decrement.

    Returns
    -------
    float
    """
    return x - 1
